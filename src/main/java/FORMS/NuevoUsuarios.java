    private void cargarTabla() {
        modeloTabla.setRowCount(0);
        for (LOGICA.UsuarioClass u : usuarioDAO.listar()) {
            Object[] fila = new Object[3];
            fila[0] = u.getIdUsuario();
            fila[1] = u.getUsuario();
            fila[2] = u.getNombre();
            modeloTabla.addRow(fila);
        }
    }

